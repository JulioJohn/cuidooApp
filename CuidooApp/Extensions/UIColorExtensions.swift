import UIKit

extension UIColor{
    class var cuidooPurple: UIColor{ return UIColor(red: 99.0/255, green: 78/255, blue: 255/255, alpha: 1)}
    class var cuidooLightGrey: UIColor{ return UIColor(red: 249/255, green: 249/255, blue: 249/255, alpha: 1)}
    class var cuidooPink: UIColor{ return UIColor(red: 255.0/255, green: 100/255, blue: 100/255, alpha: 1)}
}
